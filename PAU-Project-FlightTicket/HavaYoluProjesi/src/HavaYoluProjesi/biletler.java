package HavaYoluProjesi;


public class biletler {
    int ucakid;
    int uyeid;
    int koltukid;

    public biletler(int ucakid, int uyeid, int koltukid) {
        this.ucakid = ucakid;
        this.uyeid = uyeid;
        this.koltukid = koltukid;
    }

    public int getUcakid() {
        return ucakid;
    }

    public void setUcakid(int ucakid) {
        this.ucakid = ucakid;
    }

    public int getUyeid() {
        return uyeid;
    }

    public void setUyeid(int uyeid) {
        this.uyeid = uyeid;
    }

    public int getKoltukid() {
        return koltukid;
    }

    public void setKoltukid(int koltukid) {
        this.koltukid = koltukid;
    }

    
    
    
    
    
}
